export function namaSecurity(nama) {
	if (nama === 'imran') {
		return 'Imran Mokodompit';
	} else if (nama === 'faisal') {
		return 'Faisal Manopode';
	} else if (nama === 'karter') {
		return 'Karter Tanduk';
	} else if (nama === 'jerry') {
		return 'Jerry Legoh';
	} else if (nama === 'refi') {
		return 'Refiansa Mokodompit';
	} else if (nama === 'jonly') {
		return 'Jonly Tulandi';
	}
}
